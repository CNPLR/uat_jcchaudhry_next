// Utility to reduce a number to a single digit or special number
export const reduceToSingleDigit = (num: number): number => {
  if (!Number.isFinite(num)) return 0;

  // Handle negative & decimal numbers
  num = Math.abs(Math.floor(num));

  let sum = 0;
  while (num > 0) {
    sum += num % 10;
    num = Math.floor(num / 10);
  }

  return sum < 10 ? sum : reduceToSingleDigit(sum);
};

// Calculate soul urge number
export const calculateSoulurgeNumber = (name: any) => {
    const nameLower = name.toLowerCase();
    let vowelSum = 0;
    let consonantSum = 0;

    for (const char of nameLower) {
        if (vowelValues[char]) vowelSum += vowelValues[char];
        else if (consonantValues[char]) consonantSum += consonantValues[char];
    }
    return consonantSum
};

export function calculateDestiny(dob: any) {
    const [yearStr, monthStr, dayStr] = dob?.toString()?.split("-");
    // Reduce digits of a number string to single digit (or master numbers if needed)
    const digitSum = (str: any) => str?.split("").map(Number).reduce((a: any, b: any) => a + b, 0);
    // Step 1: sum digits of each part
    let yearSum = digitSum(yearStr);   // 2+0+0+1 = 3
    let monthSum = digitSum(monthStr); // 1+1 = 2
    let daySum = digitSum(dayStr);     // 0+6 = 6
    let year = digitSum(yearSum.toString())
    const destiny = daySum + monthSum + year;
    return destiny;
}

export function calculateNameNumber(fullName: any) {
    // Helper: sum letters of a word
    const sumWord = (word: any) => {
        let sum = word.toUpperCase().replace(/[^A-Z]/g, "").split("").reduce((acc: any, char: any) => acc + (charValues[char] || 0), 0);
        // Reduce to single digit or master number (11, 22, 33 can be kept if needed)
        while (sum > 9 && ![11, 22, 33].includes(sum)) { sum = sum.toString().split("").reduce((acc: any, digit: any) => acc + parseInt(digit), 0); }
        return sum;
    };

    const parts = fullName.trim().split(/\s+/); // Split name into words
    const reducedParts = parts.map(sumWord); // Reduce each word individually
    let total = reducedParts.reduce((acc: any, num: any) => acc + num, 0); // Sum all reduced parts
    return total;
}